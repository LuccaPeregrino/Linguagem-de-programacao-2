    // Consultar resultados
    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        try {
            out.writeInt(36);
            out.writeUTF(login);
            out.writeUTF(senha);
            in.readBoolean();

            int tam = in.readInt();
            byte[] bytes = new byte[tam];
            in.read(bytes);
            professor = (Professor) Serializador.converterByteArrayParaObjeto(bytes);

        } catch (Exception e) {
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(this, "Falha no servidor.", "Erro", JOptionPane.ERROR_MESSAGE);
        }

        JFrameConsulta janelaConsulta = new JFrameConsulta(professor, in, out);
        pack();
        janelaConsulta.setVisible(true);
        janelaConsulta.setDefaultCloseOperation(JFrameInicial.DISPOSE_ON_CLOSE);
    }//GEN-LAST:event_jMenuItem3ActionPerformed

