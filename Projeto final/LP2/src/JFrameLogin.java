    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jTextField1.getText().equals("") ||  jTextField2.getText().equals("") ){
            JOptionPane.showMessageDialog(this, "Peencha antes de se cadastrar.", "Erro", JOptionPane.ERROR_MESSAGE);
        } else{
            try {
                out.writeInt(89);
                out.writeUTF(jTextField1.getText());
                out.writeUTF(jTextField2.getText());
                
                jTextField1.setText("");
                jTextField2.setText("");
                
                if(in.readBoolean()){
                    JOptionPane.showMessageDialog(this, "Usu치rio cadastrado com sucesso.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(this, "Usu치rio j치 existe.", "Erro", JOptionPane.ERROR_MESSAGE);
                }
                
            } catch (Exception ex) {
               JOptionPane.showMessageDialog(this, "Falha no servidor.", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

